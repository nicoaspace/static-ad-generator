def build_gallery(brand_dir: Path, brand_name: str, results: list[dict]) -> None:
    """Write outputs/index.html with a visual grid of every generated ad."""
    rows = ""
    for r in results:
        if not r["saved"]:
            continue
        rows += f"<h2>{r['num']:02d} — {r['name']}</h2>\n<div class='row'>\n"
        for p in r["saved"]:
            rel = Path(p).relative_to(brand_dir / "outputs")
            rows += (
                f"  <figure>"
                f"<img src='{rel}' loading='lazy'>"
                f"<figcaption>{Path(p).name}</figcaption>"
                f"</figure>\n"
            )
        rows += "</div>\n"

    generated_at = datetime.now().strftime("%Y-%m-%d %H:%M")
    html = f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<title>{brand_name} — Static Ad Gallery</title>
<style>
  body       {{ font-family: sans-serif; background: #111; color: #eee; padding: 1rem 2rem; margin: 0; }}
  h1         {{ border-bottom: 2px solid #333; padding-bottom: .5rem; }}
  h1 span    {{ font-size: .65em; font-weight: 400; color: #666; }}
  h2         {{ margin: 2rem 0 .4rem; color: #888; font-size: .85rem; text-transform: uppercase; letter-spacing: .07em; }}
  .row       {{ display: flex; flex-wrap: wrap; gap: 10px; margin-bottom: .5rem; }}
  figure     {{ margin: 0; }}
  img        {{ max-height: 260px; border-radius: 6px; display: block; cursor: zoom-in; }}
  figcaption {{ font-size: .65rem; color: #555; margin-top: 3px; }}
</style>
</head>
<body>
<h1>{brand_name} — Static Ads <span>generated {generated_at}</span></h1>
{rows}
</body>
</html>"""

    gallery_path = brand_dir / "outputs" / "index.html"
    gallery_path.parent.mkdir(parents=True, exist_ok=True)
    gallery_path.write_text(html, encoding="utf-8")
    print(f"\n  Gallery -> {gallery_path}")


