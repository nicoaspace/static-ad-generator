def scan_brand_assets(brand_dir: Path) -> dict[str, list[Path]]:
    """Scan brand-assets/ subfolders and return {category: [image_paths]}."""
    assets_dir = brand_dir / "brand-assets"
    scan: dict[str, list[Path]] = {cat: [] for cat in ASSET_CATEGORIES}
    if not assets_dir.exists():
        return scan
    for cat in ASSET_CATEGORIES:
        cat_dir = assets_dir / cat
        if cat_dir.is_dir():
            for ext in IMAGE_EXTENSIONS:
                scan[cat].extend(sorted(cat_dir.glob(ext)))
    return scan


def check_template_eligibility(
    prompts: list[dict],
    asset_scan: dict[str, list[Path]],
) -> list[dict]:
    """
    For each service template, determine eligibility based on available assets.
    Returns list of {num, name, status, missing_required, missing_preferred}.
    Status: 'ready' | 'partial' | 'blocked'
    """
    populated = {cat for cat, paths in asset_scan.items() if paths}
    results = []
    for p in prompts:
        num  = p["template_number"]
        name = p.get("template_name", SERVICE_TEMPLATE_NAMES.get(num, f"template-{num}"))
        required  = set(p.get("required_assets", []))
        preferred = set(p.get("preferred_assets", []))

        missing_req  = required - populated
        missing_pref = preferred - populated

        if missing_req:
            status = "blocked"
        elif missing_pref:
            status = "partial"
        else:
            status = "ready"

        results.append({
            "num": num,
            "name": name,
            "status": status,
            "missing_required": sorted(missing_req),
            "missing_preferred": sorted(missing_pref),
        })
    return results


def load_assets_for_template(
    template: dict,
    asset_scan: dict[str, list[Path]],
) -> list[tuple[str, Image.Image]]:
    """
    Load only the relevant subfolder images for a specific service template.
    Uses required_assets + preferred_assets to pick which categories to load.
    """
    categories = set(template.get("required_assets", []))
    categories |= set(template.get("preferred_assets", []))
    images: list[tuple[str, Image.Image]] = []
    for cat in sorted(categories):
        for path in asset_scan.get(cat, []):
            try:
                img = Image.open(path).convert("RGB")
                images.append((path.name, img))
            except Exception:
                pass
    return images


def print_recommend_report(
    eligibility: list[dict],
    asset_scan: dict[str, list[Path]],
) -> None:
    """Print a formatted --recommend report."""
    print("\n  Brand assets scan:")
    for cat in ASSET_CATEGORIES:
        count = len(asset_scan[cat])
        icon  = "✓" if count else "✗"
        label = f"{count} image(s)" if count else "empty"
        print(f"    {cat + '/':<16} {icon}  {label}")

    ready   = [e for e in eligibility if e["status"] == "ready"]
    partial = [e for e in eligibility if e["status"] == "partial"]
    blocked = [e for e in eligibility if e["status"] == "blocked"]
    total   = len(eligibility)

    print(f"\n  Template eligibility ({total} total):")
    if ready:
        nums = ", ".join(f"S{e['num']}" for e in ready)
        print(f"    ✓  READY   ({len(ready)}): {nums}")
    if partial:
        nums = ", ".join(f"S{e['num']}" for e in partial)
        cats = sorted({c for e in partial for c in e['missing_preferred']})
        print(f"    ⚠  PARTIAL ({len(partial)}): {nums} — would improve with: {', '.join(c + '/' for c in cats)}")
    if blocked:
        nums = ", ".join(f"S{e['num']}" for e in blocked)
        cats = sorted({c for e in blocked for c in e['missing_required']})
        print(f"    ✗  BLOCKED ({len(blocked)}): {nums} — missing required: {', '.join(c + '/' for c in cats)}")

    # Actionable recommendation
    all_missing = set()
    for e in blocked + partial:
        all_missing.update(e["missing_required"])
        all_missing.update(e["missing_preferred"])
    if all_missing:
        unlockable = len(blocked) + len(partial)
        print(f"\n  Recommendation: Add assets to {', '.join(c + '/' for c in sorted(all_missing))} "
              f"to unlock/improve {unlockable} more template(s).")
    else:
        print(f"\n  All {total} templates are fully eligible. Ready to generate.")
    print()


