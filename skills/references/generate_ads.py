def main() -> None:
    available = list_brands()

    parser = argparse.ArgumentParser(
        description="Generate static ads with the configured image provider",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples (product):\n"
            "  python skills/references/generate_ads.py --brand lmnt --type product\n"
            "  python skills/references/generate_ads.py --brand lmnt --type product --templates 1,7,13\n"
            "  python skills/references/generate_ads.py --brand lmnt --type product --dry-run\n"
            "\n"
            "Examples (service/SaaS):\n"
            "  python skills/references/generate_ads.py --brand notion --type service\n"
            "  python skills/references/generate_ads.py --brand notion --type service --recommend\n"
            "  python skills/references/generate_ads.py --brand notion --type service --dry-run\n"
        ),
    )
    parser.add_argument(
        "--brand",
        required=True,
        metavar="NAME",
        help=(
            f"Brand folder name under brands/  "
            f"(available: {', '.join(available) if available else 'none yet'})"
        ),
    )
    parser.add_argument(
        "--type",
        required=True,
        choices=["product", "service"],
        dest="brand_type",
        help="Brand type: 'product' (physical goods) or 'service' (SaaS/digital)",
    )
    parser.add_argument(
        "--templates",
        metavar="1,7,13",
        help="Comma-separated template numbers to run (default: all)",
    )
    parser.add_argument(
        "--resolution",
        default=DEFAULT_RESOLUTION,
        choices=list(get_resolution_choices()),
        help=f"Image resolution (default: {DEFAULT_RESOLUTION})",
    )
    parser.add_argument(
        "--variations",
        type=int,
        default=DEFAULT_VARIATIONS,
        metavar="N",
        help=f"Variations per template (default: {DEFAULT_VARIATIONS})",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Preview what would run — no API calls made",
    )
    parser.add_argument(
        "--recommend",
        action="store_true",
        help="(service only) Scan brand-assets/ and report which templates are eligible",
    )
    args = parser.parse_args()

    # ── Select template map based on --type ───────────────────────────────────
    is_service   = args.brand_type == "service"
    template_map = SERVICE_TEMPLATE_NAMES if is_service else TEMPLATE_NAMES

    # ── Resolve brand dir ─────────────────────────────────────────────────────
    brand_dir = BRANDS_ROOT / args.brand
    if not brand_dir.exists():
        sys.exit(
            f"ERROR: brands/{args.brand}/ not found.\n"
            f"  Available: {', '.join(available) or 'none'}\n"
            f"  Expected path: {brand_dir}"
        )

    prompts_file = brand_dir / "prompts.json"
    # --recommend on a service brand works before Phase 2 (no prompts.json needed)
    recommend_no_prompts = args.recommend and is_service and not prompts_file.exists()
    if not prompts_file.exists() and not recommend_no_prompts:
        sys.exit(
            f"ERROR: prompts.json not found in brands/{args.brand}/\n"
            "  Run Phase 2 first to generate it.\n"
            "  Tip: you can still scan your assets now with --recommend."
        )

    # ── API key ───────────────────────────────────────────────────────────────
    missing = missing_image_api_key()
    if missing and not args.dry_run and not args.recommend:
        provider = "OpenRouter" if is_public_version() else "Google AI Studio"
        sys.exit(
            f"ERROR: {missing} not set.\n"
            f"  Add it to env/.env.local for {provider} image generation.\n"
            f"  File: {ENV_FILE}"
        )

    # ── Load prompts ──────────────────────────────────────────────────────────
    if recommend_no_prompts:
        # Build synthetic prompt list from static asset requirements
        all_prompts = [
            {
                "template_number": num,
                "template_name": SERVICE_TEMPLATE_NAMES.get(num, f"template-{num}"),
                "required_assets": SERVICE_TEMPLATE_ASSETS[num]["required"],
                "preferred_assets": SERVICE_TEMPLATE_ASSETS[num]["preferred"],
            }
            for num in sorted(SERVICE_TEMPLATE_ASSETS)
        ]
        brand_name = args.brand
        product    = ""
    else:
        data        = json.loads(prompts_file.read_text(encoding="utf-8"))
        all_prompts = data["prompts"]
        brand_name  = data.get("brand", args.brand)
        product     = data.get("product", "")

    if args.templates:
        requested   = {int(t.strip()) for t in args.templates.split(",")}
        all_prompts = [p for p in all_prompts if p["template_number"] in requested]
        if not all_prompts:
            sys.exit(f"ERROR: No prompts matched template numbers: {args.templates}")

    # ── Load reference images (branched by type) ──────────────────────────────
    ref_images   = []  # product: flat list used for all templates
    asset_scan   = {}  # service: per-category scan

    if is_service:
        asset_scan = scan_brand_assets(brand_dir)
        total_assets = sum(len(v) for v in asset_scan.values())
        if total_assets:
            print(f"\nScanning brand-assets/ for brands/{args.brand}/")
            for cat in ASSET_CATEGORIES:
                count = len(asset_scan[cat])
                if count:
                    print(f"    {cat + '/':<16} {count} image(s)")
        else:
            print(f"\nNo brand-assets/ subfolders populated -- templates will run text-only.")

        # ── --recommend mode (service only) ───────────────────────────────────
        if args.recommend:
            eligibility = check_template_eligibility(all_prompts, asset_scan)
            print_recommend_report(eligibility, asset_scan)
            return
    else:
        img_dir = brand_dir / "product-images"
        if img_dir.exists():
            print(f"\nLoading product images from brands/{args.brand}/product-images/")
            ref_images = load_product_images(img_dir)
        else:
            print(f"\nNo product-images/ folder -- all templates will run text-only.")

        if args.recommend:
            print("\n  --recommend is only available for --type service. Ignored.")

    # ── Print run summary ─────────────────────────────────────────────────────
    total = len(all_prompts) * args.variations
    sep = "=" * 54
    refs_label = (
        f"{sum(len(v) for v in asset_scan.values())} brand asset(s) across {sum(1 for v in asset_scan.values() if v)} categorie(s)"
        if is_service
        else f"{len(ref_images)} product image(s) loaded"
    )
    print(f"\n{sep}")
    print(f"  Brand    : {brand_name}" + (f"  /  {product}" if product else ""))
    print(f"  Type     : {args.brand_type}")
    print(f"  Model    : {MODEL}")
    if is_public_version():
        print(f"  Latency  : ~{get_public_image_latency_seconds()}s per image")
    print(f"  Templates: {len(all_prompts)}  x  {args.variations} variation(s)  =  {total} images")
    print(f"  Refs     : {refs_label}")
    print(f"  Res      : {args.resolution}  |  Output: brands/{args.brand}/outputs/")
    if args.dry_run:
        print(f"  Mode     : DRY RUN -- no API calls")
    print(f"{sep}\n")

    if args.dry_run:
        # For service dry-run, also show per-template eligibility
        eligibility_map = {}
        if is_service:
            for e in check_template_eligibility(all_prompts, asset_scan):
                eligibility_map[e["num"]] = e

        for p in all_prompts:
            num    = p["template_number"]
            name   = template_map.get(num, p.get("template_name", f"template-{num}"))
            aspect = p.get("aspect_ratio", "4:5")

            if is_service:
                # Per-template asset loading
                t_assets = load_assets_for_template(p, asset_scan)
                mode = f"+ {len(t_assets)} asset(s)" if t_assets else "text-only"
                elig = eligibility_map.get(num)
                if elig:
                    if elig["status"] == "ready":
                        status = "✓ READY"
                    elif elig["status"] == "partial":
                        status = f"⚠ better with {', '.join(c + '/' for c in elig['missing_preferred'])}"
                    else:
                        status = f"✗ BLOCKED (needs {', '.join(c + '/' for c in elig['missing_required'])})"
                    print(f"  [{num:02d}] {name:<35} {aspect}  {mode:<20} {status}")
                else:
                    print(f"  [{num:02d}] {name:<35} {aspect}  {mode}")
            else:
                needs = p.get("needs_product_images", False)
                mode  = f"+ {len(ref_images)} ref(s)" if (needs and ref_images) else "text-only"
                print(f"  [{num:02d}] {name:<35} {aspect}  {mode}")
        print()
        return

    # ── Generate ──────────────────────────────────────────────────────────────
    completed = 0
    failed    = 0
    results   = []
    total_images = len(all_prompts) * args.variations
    images_done = 0

    emit_progress(35, f"Generating {total_images} image(s)…", phase="phase3", totalImages=total_images)

    for prompt_obj in all_prompts:
        num         = prompt_obj["template_number"]
        name        = template_map.get(num, prompt_obj.get("template_name", f"template-{num}"))
        prompt_text = prompt_obj["prompt"]
        aspect      = prompt_obj.get("aspect_ratio", "4:5")

        if is_service:
            refs = load_assets_for_template(prompt_obj, asset_scan)
        else:
            needs_ref = prompt_obj.get("needs_product_images", False)
            refs = ref_images if (needs_ref and ref_images) else []

        mode  = f"+ {len(refs)} ref(s)" if refs else "text-only"

        out_dir = brand_dir / "outputs" / f"{num:02d}-{name}"
        out_dir.mkdir(parents=True, exist_ok=True)
        (out_dir / "prompt.txt").write_text(prompt_text, encoding="utf-8")

        print(f"[{num:02d}] {name}  ({aspect}, {mode})")

        saved = []

        for v in range(1, args.variations + 1):
            images_done += 1
            pct = 35 + int((images_done / max(total_images, 1)) * 60)
            emit_progress(
                pct,
                f"Template {num} — variation {v}/{args.variations}",
                phase="phase3",
                template=num,
                variation=v,
                completedImages=images_done,
                totalImages=total_images,
            )
            print(f"  -> v{v}/{args.variations} ...", end=" ", flush=True)
            try:
                img_bytes = call_api(
                    prompt_text, aspect, args.resolution, refs, seed=v
                )

                if not img_bytes:
                    print("FAIL  no image in response")
                    failed += 1
                    continue

                filename  = f"{name}_v{v}.png"
                save_path = out_dir / filename
                save_path.write_bytes(img_bytes)
                saved.append(str(save_path))
                print(f"OK  {filename}")
                completed += 1

            except Exception as exc:
                print(f"FAIL  {exc}")
                failed += 1

            if v < args.variations:
                time.sleep(RATE_LIMIT_PAUSE)

        results.append({"num": num, "name": name, "saved": saved})

    # ── Gallery + final summary ───────────────────────────────────────────────
    emit_progress(95, "Building gallery…", phase="phase3")
    build_gallery(brand_dir, brand_name, results)

    sep2 = "-" * 54
    print(f"\n{sep2}")
    print(f"  Done.  OK {completed} saved   FAIL {failed}   total {completed + failed}")
    print(f"{sep2}\n")

    if failed > 0 and completed == 0:
        sys.exit(1)


if __name__ == "__main__":
    main()
