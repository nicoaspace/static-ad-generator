def list_brands() -> list[str]:
    """Return brand names that have a prompts.json ready."""
    if not BRANDS_ROOT.exists():
        return []
    return sorted(
        d.name for d in BRANDS_ROOT.iterdir()
        if d.is_dir() and (d / "prompts.json").exists()
    )


def load_images_from_dir(image_dir: Path, label: str = "") -> list[tuple[str, Image.Image]]:
    """Load all images from a directory as (filename, PIL.Image) pairs."""
    images = []
    for ext in IMAGE_EXTENSIONS:
        for path in sorted(image_dir.glob(ext)):
            try:
                img = Image.open(path).convert("RGB")
                images.append((path.name, img))
                prefix = f"    [{label}] " if label else "    "
                print(f"{prefix}+ {path.name}")
            except Exception as exc:
                print(f"    ! Could not load {path.name}: {exc}")
    return images


def load_product_images(product_images_dir: Path) -> list[tuple[str, Image.Image]]:
    """Load product-images/ folder as (filename, PIL.Image) pairs. Product pipeline only."""
    return load_images_from_dir(product_images_dir)


